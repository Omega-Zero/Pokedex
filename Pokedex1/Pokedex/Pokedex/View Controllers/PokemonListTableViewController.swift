import UIKit

class PokemonListTableViewController: UITableViewController {
    
    override func viewDidLoad() {
        super.viewDidLoad()
    }
    
    // MARK: - Table view data source
    
    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return PokemonController.instance.count
    }
    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        
        guard let cell = tableView.dequeueReusableCell(withIdentifier: "pokemonCell") else {
            preconditionFailure("Failed to find cell, check ids.")
        }
        
        if let pokemon = PokemonController.instance.pokemonAtIndex(indexPath.row){
            cell.textLabel?.text = pokemon.name
        }else{
            cell.textLabel?.text = "???"
        }
        
        return cell
    }
    
